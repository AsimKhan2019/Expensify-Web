import en from '../locale/en';
import es from '../locale/es';

export const setLanguage = (locale = 'en') => {
  return (dispatch, getState) => {
    let dictionary = {};
    if(locale == 'es')
      dictionary = es;
    else
      dictionary = en;

    return dispatch({
      type: 'SET_LANGUAGE',
      locale,
      dictionary
    });
  };
};
